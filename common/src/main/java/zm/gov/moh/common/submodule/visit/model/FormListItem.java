package zm.gov.moh.common.submodule.visit.model;

public class FormListItem {
    private String name;
    private int status;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return status;
    }

    public void setId(int id) {
        this.status = id;
    }
}
